export function getTranslationForBodyPart(partName: string, t: Record<string, string>): string {
  const norm = partName?.toLowerCase().trim();
  if (norm === 'head') return t.headRegion || partName;
  if (norm === 'neck') return t.neckRegion || partName;
  if (norm === 'shoulder') return t.shoulderRegion || partName;
  if (norm === 'chest') return t.chestRegion || partName;
  if (norm === 'upper back') return t.upperBackRegion || partName;
  if (norm === 'core' || norm === 'core / abs') return t.coreRegion || partName;
  if (norm === 'lower back') return t.lowerBackRegion || partName;
  if (norm === 'hip') return t.hipRegion || partName;
  if (norm === 'quad') return t.quadRegion || partName;
  if (norm === 'hamstring') return t.hamstringRegion || partName;
  if (norm === 'knee') return t.kneeRegion || partName;
  if (norm === 'calf') return t.calfRegion || partName;
  if (norm === 'lower leg' || norm === 'shin') return t.lowerLegRegion || partName;
  if (norm === 'foot') return t.footRegion || partName;
  if (norm === 'ankle') return t.ankleRegion || partName;
  if (norm === 'elbow') return t.elbowRegion || partName;
  return partName;
}
